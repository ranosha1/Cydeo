package practice;

public class Casting {

    public static void main(String[] args) {

        //implicit casting
        int a=10;
        double b=a;
        System.out.println(a);
        System.out.println(b);

        //explicit casting
        double c=20.5;
        int d=(int)c;
        System.out.println(c);
        System.out.println(d);



    }
}
