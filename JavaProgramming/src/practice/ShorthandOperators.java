package practice;

public class ShorthandOperators {

    public static void main(String[] args) {
        //assigning new value
        int a=100;
        a=200;
        System.out.println(a);
        System.out.println("----------");

        //addition assignment
        int b =200;
        b+=100;//300
        System.out.println(b);
        System.out.println("----------");

        //subtraction
        int c=100;
        c-=20;//80
        System.out.println(c);
        System.out.println("----------");

        //multiplication
        int d =100;
        d*=2;//200
        System.out.println(d);
        System.out.println("----------");

        //division
        int t=10;
        t/=5;//2
        System.out.println(t);
        System.out.println("----------");

        //remainder
        int f=50;
        f%=11;//6
        System.out.println(f);





    }
}
