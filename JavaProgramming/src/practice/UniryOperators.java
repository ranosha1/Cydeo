package practice;

import java.awt.color.ICC_Profile;

public class UniryOperators {
    public static void main(String[] args) {


        int x=10;
        System.out.println(++x);//11
        System.out.println(--x);//10

        int y=25;
        System.out.println(y++);//25
        System.out.println(y);//26
        System.out.println(y--);//26
        System.out.println(y);//25

    }


}
